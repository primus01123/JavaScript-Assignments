// File: for_loop_practice.js
// Task: Use for loops to print numbers.
// Requirements:
// Use console.log() for each number.
// Include comments explaining each loop.

// For loop that prints all even numbers from 2 to 10
// Start at 2, increment by 2 each time to get even numbers
for (let i = 2; i <= 10; i += 2) {
  console.log(i);
}

// For loop that prints numbers from 10 to 1 in descending order
// Start at 10, decrement by 1 until we reach 1
for (let i = 10; i >= 1; i--) {
  console.log(i);
}

